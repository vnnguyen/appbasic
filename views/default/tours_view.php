<p>Départ en minibus pour la ville portuaire de Haiphong où nous visitons la pagode de Du Hang avant le déjeuner. Nous embarquons ensuite à bord d'une navette rapide qui nous conduit jusqu'à l’île de Cat Ba (environ 1h de transfert), la plus grande île des alentours de la baie d'Along, réputée pour ses reliefs et sa forêt.</p>
<p>Balade en bateau dans la magnifique baie de Lan Ha située à l'est de l'île de Cat Ba. Moins fréquentée par les touristes que le reste de l'île, cette baie est particulièrement connue pour ses nombreuses plages et ses villages de pêcheurs. Nous voguons entre les innombrables îlots (366 en tout !), au fil des maisons flottantes de pêcheurs. Nous avons la possibilité de faire du kayak et de se baigner sur des petites plages isolées.</p>
<p>En fin de journée, nous débarquons au port de Viet Hai où nous récupérons un vélo pour rejoindre le village. Le chemin longe la mer, passe un col puis traverse une vallée entourée de pitons calcaires.</p>
<p>Installation dans les bungalows d’une auberge locale.</p>
<p>Note : si le temps le permet, ou durant la période hivernale lorsque la baignade et le kayak ne sont pas possibles, nous montons sur une colline au village de Viet Hai d’où nous avons une vue panoramique sur les baies d’Along et de Lan Ha. Temps pour la montée et la descente : 2h</p>
<p>Nuit à l'auberge.</p>
